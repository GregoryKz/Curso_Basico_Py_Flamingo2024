#Escreva um algoritmo para ler uma temperatura em graus Fahrenheit, calcular e escrever o valor correspondente em graus Celsius (baseado na fórmula abaixo):
print("Conversão de Fahrenheit para Celsius")
fahrenheit=int(input("Digite a temperatura em Fahrenheit: "))
conversao=(fahrenheit-32)*5/9
print(f"A temperatura em fahrenheit de {fahrenheit},convertidas para celsius e de :{conversao:.2f}°C")
