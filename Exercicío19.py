print("Calcularora de Segundos")
dia=int(input("Digite os dias: "))
hora=int(input("Digite a hora: "))
minutos=int(input("Digite os minutos: "))
segundos=int(input("Digite os segundos: "))
resultado=(dia*86400)+(hora*3600)+(minutos*60)+segundos
print("As horas convertidas em segundos são:",resultado)
